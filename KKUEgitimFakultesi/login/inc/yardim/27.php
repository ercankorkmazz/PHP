<div class="CollapsiblePanelContent">
	<p>Albüme fotoðraf eklemek için "<strong>Galeri</strong>" menüsüne týklayýn.</p>
    <p>Açýlan ekranýn alt tarafýnda "<strong>Sisteme Kayýtlý Albümler</strong>" listelenecektir. Fotoðraf eklemek istediðiniz albümü seçiniz.</p>
    <p>Açýlan ekranýn "<strong>Yeni Fotoðraf Yükle</strong>" bölümündeki "<strong>Gözat</strong>" butonuna týklayarak istediðimiz fotoðrafý seçiniz. Her seferde en fazla 1 fotoðraf yükleyebilirsiniz.</p>
    <p>Yüklenmek istenen fotoðrafý seçildikten sonra "<strong>Fotoðrafý Yükle</strong>" butonuna týklamanýz yeterlidir.</p>
